Функції вищого порядку
Функція вищого порядку - функція, що приймає як аргументи інші функції або
повертає іншу функцію як результат. Основна ідея полягає в тому, що функції
мають той самий статус, що й інші об'єкти даних.
Каррування або карринг (англ. currying) - перетворення функції від багатьох
аргументів на функцію, що бере свої аргументи по одному. Це перетворення було
запроваджено М. Шейнфінкелем і Г. Фреге й отримало свою назву на честь
Х. Каррі. Каррі.

Декоратор у Python - функція, яка приймає як параметр іншу функцію (або клас)
і повертає нову, модифіковану функцію (або клас), яка її замінює.
Крім того, поняття функцій вищого порядку часто застосовується і для створення
декораторів: часто потрібно, щоб декоратор приймав ще якісь параметри, крім
модифікованого об'єкта. У такому разі створюється функція, що створює і
повертає декоратор, а під час застосування декоратора замість вказівки імені
функції-декоратора ця функція викликається.